def mirror_square(sq: int) -> int:
    """Mirror square vertically for Black's perspective."""
    rank = sq // 8
    file = sq % 8
    return (7 - rank) * 8 + file


# Create Black PSTs by mirroring
PST_BLACK = {
    piece_type: [PST_WHITE[piece_type][mirror_square(sq)] for sq in range(64)]
    for piece_type in PST_WHITE
}
